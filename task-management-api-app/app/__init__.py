# ============================================================================
# APP PACKAGE INITIALIZATION
# ============================================================================
# This file makes the "app" folder a Python package.
#
# Technical Term: __init__.py
# - When Python sees a folder with __init__.py, it treats it as a "package"
# - A package is a collection of modules (Python files) that can be imported
# - Without this file, Python won't recognize "app" as a package
#
# Example:
#   from app.main import app     # This works because of __init__.py
#   from app.models import Task  # This works because of __init__.py
